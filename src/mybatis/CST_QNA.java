package mybatis;

public class CST_QNA {
	private String qna_idx, qna_ctg, id, pwd, title, content, filename, qna_hit, regdate;

	public String getQna_ctg() {
		return qna_ctg;
	}

	public void setQna_ctg(String qna_ctg) {
		this.qna_ctg = qna_ctg;
	}

	public String getQna_idx() {
		return qna_idx;
	}

	public void setQna_idx(String qna_idx) {
		this.qna_idx = qna_idx;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getQna_hit() {
		return qna_hit;
	}

	public void setQna_hit(String qna_hit) {
		this.qna_hit = qna_hit;
	}

	public String getRegdate() {
		return regdate;
	}

	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	public String getFilename() {
		return filename;
	}

	public void setFilename(String filename) {
		this.filename = filename;
	}
	
}
